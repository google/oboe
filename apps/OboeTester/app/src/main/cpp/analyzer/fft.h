#ifndef FFT_H
#define FFT_H

#include <complex>
#include <vector>

const double PI = 3.141592653589793238460;

typedef std::complex<double> Complex;
typedef std::vector<Complex> CVector;

// Radix-2 Cooley-Tukey FFT
void fft(CVector &a) {
    int n = a.size();
    if (n <= 1) return;

    CVector a0(n / 2), a1(n / 2);
    for (int i = 0; 2 * i < n; i++) {
        a0[i] = a[2*i];
        a1[i] = a[2*i+1];
    }
    fft(a0);
    fft(a1);

    double ang = 2 * PI / n;
    Complex w(1), wn(cos(ang), sin(ang));
    for (int i = 0; 2 * i < n; i++) {
        a[i] = a0[i] + w * a1[i];
        a[i + n / 2] = a0[i] - w * a1[i];
        w *= wn;
    }
}

#endif // FFT_H
